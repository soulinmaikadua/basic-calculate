const addition = (a = 0, b = 0) => {
  return a + b;
};

const subtraction = (a = 0, b = 0) => {
  return a - b;
};

const multiply = (a = 0, b = 0) => {
  return a * b;
};
const division = (a = 0, b = 0) => {
  return a / b;
};
module.exports = {
  addition,
  subtraction,
  multiply,
  division,
};
